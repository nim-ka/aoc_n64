.macro IPL3FontConvert l0, l1, l2, l3, l4, l5, l6, l7, l8, l9, l10, l11, l12, l13
    .byte (\l0 >> 5) & 0xff
    .byte ((\l0 << 3) + (\l1 >> 10)) & 0xff
    .byte ((\l1 >> 2)) & 0xff
    .byte ((\l1 << 6) + (\l2 >> 7)) & 0xff
    .byte ((\l2 << 1) + (\l3 >> 12)) & 0xff
    .byte ((\l3 >> 4)) & 0xff
    .byte ((\l3 << 4) + (\l4 >> 9)) & 0xff
    .byte ((\l4 >> 1)) & 0xff
    .byte ((\l4 << 7) + (\l5 >> 6)) & 0xff
    .byte ((\l5 << 2) + (\l6 >> 11)) & 0xff
    .byte ((\l6 >> 3)) & 0xff
    .byte ((\l6 << 5) + (\l7 >> 8)) & 0xff
    .byte (\l7) & 0xff
    .byte ((\l8 >> 5)) & 0xff
    .byte ((\l8 << 3) + (\l9 >> 10)) & 0xff
    .byte ((\l9 >> 2)) & 0xff
    .byte ((\l9 << 6) + (\l10 >> 7)) & 0xff
    .byte ((\l10 << 1) + (\l11 >> 12)) & 0xff
    .byte ((\l11 >> 4)) & 0xff
    .byte ((\l11 << 4) + (\l12 >> 9)) & 0xff
    .byte ((\l12 >> 1)) & 0xff
    .byte ((\l12 << 7) + (\l13 >> 6)) & 0xff
    .byte (\l13 << 2) & 0xff
.endm

